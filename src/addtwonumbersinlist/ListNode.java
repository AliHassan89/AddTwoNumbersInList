package addtwonumbersinlist;

/**
 *
 * @author Ali
 */
public class ListNode 
{
    public int val;
    public ListNode next;
    public ListNode(int x) 
    {
        val = x;
        next = null;
    }
    
    public ListNode()
    {
        val = 0;
        next = null;
    }
}
